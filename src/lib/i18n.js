export function createT(lang) {
  const table = dict[lang] || dict.de
  return function t(key, vars) {
    let s = table[key] ?? dict.de[key] ?? key
    if (vars) {
      for (const [k, v] of Object.entries(vars)) {
        s = s.replaceAll(`{${k}}`, String(v))
      }
    }
    return s
  }
}

